package loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i=0;  //initialization is to be done outside the loop
		
		while(i>=10) //Condition is to be given here; dice example
		{
			System.out.println(i);
			i++;  //it can be increment or decrement depending on scenario
			
		}
		
	}

}


