package conditionalStatements;

public class MathameticalOperators {

	public static void main(String[] args) {
		double a= 20;
		double b = 10;
//		double sum;
		
		System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(a/b);  // gives us the quotient
		System.out.println(a*b);
		System.out.println(a%b); //gives us the remainder
		
	}

}
